/**
 * Sync brand-ui + brand-portal into brand-static/ + brandStaticBundle.js
 */
const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "../..");
const OUT = path.join(ROOT, "brand-static");
const BUNDLE = path.join(ROOT, "src/features/brands/brandStaticBundle.js");

const COPY_MAP = [
  { from: "brand-ui/css", to: "shared/css" },
  { from: "brand-ui/js", to: "shared/js" },
  { from: "brand-portal", to: "." },
];

function rmrf(dir) {
  if (!fs.existsSync(dir)) return;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, entry.name);
    if (entry.isDirectory()) rmrf(p);
    else fs.unlinkSync(p);
  }
  fs.rmdirSync(dir);
}

function copyDir(src, dest) {
  fs.mkdirSync(dest, { recursive: true });
  for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
    const from = path.join(src, entry.name);
    const to = path.join(dest, entry.name);
    if (entry.isDirectory()) copyDir(from, to);
    else fs.copyFileSync(from, to);
  }
}

function walkFiles(dir, base = dir, acc = []) {
  if (!fs.existsSync(dir)) return acc;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, entry.name);
    if (entry.isDirectory()) walkFiles(p, base, acc);
    else acc.push({ key: path.relative(base, p).replace(/\\/g, "/"), full: p });
  }
  return acc;
}

const MIME = {
  ".html": "text/html; charset=utf-8",
  ".css": "text/css; charset=utf-8",
  ".js": "application/javascript; charset=utf-8",
  ".svg": "image/svg+xml",
};

function main() {
  if (fs.existsSync(OUT)) rmrf(OUT);
  fs.mkdirSync(OUT, { recursive: true });

  for (const { from, to } of COPY_MAP) {
    const src = path.join(ROOT, from);
    if (!fs.existsSync(src)) {
      console.warn(`skip missing: ${from}`);
      continue;
    }
    copyDir(src, path.join(OUT, to));
  }

  const files = walkFiles(OUT, OUT);
  const bundle = {};
  for (const { key, full } of files) {
    const ext = path.extname(full);
    bundle[key] = {
      contentType: MIME[ext] || "application/octet-stream",
      body: fs.readFileSync(full, "utf8"),
    };
  }

  fs.writeFileSync(
    BUNDLE,
    `// AUTO-GENERATED by scripts/utils/sync-brand-static.js — do not edit\nexport const BRAND_STATIC_BUNDLE = ${JSON.stringify(bundle, null, 0)};\n`,
    "utf8"
  );

  console.log(`brand-static: ${files.length} files → ${OUT}`);
}

main();
